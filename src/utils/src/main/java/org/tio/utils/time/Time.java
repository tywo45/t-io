public class Time {

	/**
	 * 一秒
	 */
	public static final Long SECOND_1 = 1L;

	/**
	 * 一分钟
	 */
	public static final Long	MINUTE_1	= SECOND_1 * 60L;
	/**
	 * 一小时
	 */
	public static final Long	HOUR_1		= MINUTE_1 * 60L;
	/**
	 * 一天
	 */
	public static final Long	DAY_1		= HOUR_1 * 24;

}
