    /**
     * @param serverIp   可以为空
     * @param serverPort
     * @param aioDecoder
     * @param aioEncoder
     * @param tioHandler
     * @throws IOException
     * @author tanyaowu
     */
    public TioClient(final TioClientConfig tioClientConfig) throws IOException {
        super();
        this.tioClientConfig = tioClientConfig;
        this.channelGroup = AsynchronousChannelGroup.withThreadPool(tioClientConfig.groupExecutor);

        startHeartbeatTask();
        startReconnTask();
    }

